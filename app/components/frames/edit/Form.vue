<script setup lang="ts">
const { setFlash } = useSonner()
const { loggedIn } = useAccount()
const { frame, tags, comment, shootedAt, updateFrame, refresh, processing, isSuccess, flash } = inject('framer') as UseFrameType
const { editFrameRules } = useFrameRules()

const editor: Ref = useTemplateRef('editor')

const { r$ } = useI18nRegle(frame, editFrameRules)

// console.log(frame)
// console.log(frame.tags)
// console.log(frame.tag_list)

const onEditClick = async () => {
  if (editor.value?.getText().replace(/\n/g, '') == ''){
    frame.value.comment = ''
  }

  const { valid } = await r$.$validate()

  // console.log(frame)
  if (valid) {
    await updateFrame()

    setFlash(flash.value)
    if (isSuccess()) {
      await refresh()
      await navigateTo(`/frames/${frame?.value.id}`)
    } else if (!loggedIn.value) {
      await navigateTo(`/frames/${frame?.value.id}`)
    }
  }
}

const updateContent = (content: string) => {
  if (editor.value?.getText().replace(/\n/g, '') != ''){
    frame.value.comment = content
  } else {
    editor.value?.clearContents()
  }
}
</script>

<template>
  <form>
    <div class="flex justify-center">
      <table class="table table-bordered table-rounded table-fixed">
        <tbody>
          <tr>
            <td style="width: 9em;">
              <label
                for="name"
                class=""
              >{{ $t('model.frame.name') }}：</label>
            </td>
            <td>
              <input
                v-model="frame.name"
                type="text"
                placeholder=""
                class="input"
              >
              <div
                v-for="error of r$.$errors.name"
                :key="error"
              >
                <div class="text-red-500">{{ error }}</div>
              </div>
            </td>
          </tr>
          <tr>
            <td>
              <label
                for="tag_list"
                class=""
              >{{ $t('model.frame.tag_list') }}：</label>
            </td>
            <td>
              <TagEdit v-model="tags" />
              <div
                v-for="error of r$.tags.$errors"
                :key="error"
              >
                <div class="text-red-500">{{ error }}</div>
              </div>
            </td>
          </tr>
          <tr>
            <td>
              <label
                for="creator_name"
                class=""
              >{{ $t('model.frame.creator_name') }}：</label>
            </td>
            <td>
              <input
                v-model="frame.creator_name"
                type="text"
                placeholder=""
                class="input"
              >
              <div
                v-for="error of r$.$errors.creator_name"
                :key="error"
              >
                <div class="text-red-500">{{ error }}</div>
              </div>
            </td>
          </tr>
          <tr>
            <td>
              <label
                for="shooted_at"
                class=""
              >{{ $t('model.frame.shooted_at') }}：</label>
            </td>
            <td>
              <input
                v-model="shootedAt"
                type="datetime-local"
                class="input"
              >
            </td>
          </tr>
          <tr>
            <td>
              <label
                for="comment"
                class=""
              >{{ $t('model.frame.comment') }}：</label>
            </td>
            <td class="wrap-break-word">
              <div class="rounded-[5px]" style="border: 1px solid lavender;">
                <Editor
                  ref="editor"
                  v-model="comment"
                  @update="updateContent"
                />
              </div>
            </td>
          </tr>
        </tbody>
      </table>
    </div>
    <div class="flex justify-end pt-2">
      <button
        type="button"
        class="btn btn-outline btn-primary"
        :disabled="processing"
        @click="onEditClick"
      >
        {{ $t('action.model.update') }}
      </button>
    </div>
  </form>
</template>
